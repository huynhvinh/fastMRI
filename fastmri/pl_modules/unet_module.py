def off_diagonal(x):
    # return a flattened view of the off-diagonal elements of a square matrix
    n, m = x.shape
    assert n == m
    return x.flatten()[:-1].view(n - 1, n + 1)[:, 1:].flatten()


class FixMatchUnetModule(MriModule):
    """
    Unet training module.

    This can be used to train baseline U-Nets from the paper:

    J. Zbontar et al. fastMRI: An Open Dataset and Benchmarks for Accelerated
    MRI. arXiv:1811.08839. 2018.
    """

    def __init__(
        self,
        in_chans=1,
        out_chans=1,
        chans=32,
        num_pool_layers=4,
        drop_prob=0.0,
        lr=0.001,
        lr_step_size=40,
        lr_gamma=0.1,
        weight_decay=0.0,
        confidence=0.8,
        proportion=0.1,
        weights=0.5,
        **kwargs,
    ):
        """
        Args:
            in_chans (int, optional): Number of channels in the input to the
                U-Net model. Defaults to 1.
            out_chans (int, optional): Number of channels in the output to the
                U-Net model. Defaults to 1.
            chans (int, optional): Number of output channels of the first
                convolution layer. Defaults to 32.
            num_pool_layers (int, optional): Number of down-sampling and
                up-sampling layers. Defaults to 4.
            drop_prob (float, optional): Dropout probability. Defaults to 0.0.
            lr (float, optional): Learning rate. Defaults to 0.001.
            lr_step_size (int, optional): Learning rate step size. Defaults to
                40.
            lr_gamma (float, optional): Learning rate gamma decay. Defaults to
                0.1.
            weight_decay (float, optional): Parameter for penalizing weights
                norm. Defaults to 0.0.
        """
        super().__init__(**kwargs)
        self.save_hyperparameters()

        self.in_chans = in_chans
        self.out_chans = out_chans
        self.chans = chans
        self.num_pool_layers = num_pool_layers
        self.drop_prob = drop_prob
        self.lr = lr
        self.lr_step_size = lr_step_size
        self.lr_gamma = lr_gamma
        self.weight_decay = weight_decay
        self.proportion = proportion
        self.confidence = confidence
        self.weights = weights
        self.unet = Unet(
            in_chans=self.in_chans,
            out_chans=self.out_chans,
            chans=self.chans,
            num_pool_layers=self.num_pool_layers,
            drop_prob=self.drop_prob,
        )

    def forward(self, image):
        output, features = self.unet(image.unsqueeze(1))
        return output.squeeze(1), features

    def training_step(self, batch, batch_idx):

        weak_img, strong_img, target, _, _, _, _, _ = batch

        n = weak_img.shape[0]
        slice_index = int(self.proportion * n)
        # labelled images
        label_op, label_ft = self(weak_img[:slice_index])
        label_ce_loss = F.l1_loss(label_op, target[:slice_index])

        print('lable ce loss\n', label_ce_loss)

        # unlabelled images
        unlabel_weak_op, unlabel_weak_ft = self(weak_img[slice_index:])  # weak augmented
        unlabel_strong_op, unlabel_strong_ft = self(strong_img[slice_index:])  # strong augmented
        unlabelled_loss = F.l1_loss(unlabel_strong_op, unlabel_weak_op, reduction='none')

        unlabel_mask = torch.where(unlabelled_loss < self.confidence, 0, 1)

        print('unlable ce loss\n', unlabelled_loss)
        unlabel_ce_loss = unlabel_weak_op * unlabel_mask

        print('unlable ce loss after confidence\n', unlabel_ce_loss)
        unlabel_ce_loss = torch.mean(unlabel_ce_loss)

        print('unlable ce loss after mean\n', unlabel_ce_loss)

        final_loss = label_ce_loss + self.weights * unlabel_ce_loss
        print('final loss\n', final_loss)

        return final_loss

    def validation_step(self, batch, batch_idx):
        weak_img, strong_img, target, mean, std, fname, slice_num, max_value = batch

        n = weak_img.shape[0]
        slice_index = int(self.proportion * n)
        output = torch.empty_like(target)
        # labelled images
        label_op, label_ft = self(weak_img[:slice_index])
        label_ce_loss = F.l1_loss(label_op, target[:slice_index])
        output[:slice_index] = label_op
        # unlabelled images
        unlabel_weak_op, unlabel_weak_ft = self(weak_img[slice_index:])  # weak augmented
        unlabel_strong_op, unlabel_strong_ft = self(strong_img[slice_index:])  # strong augmented
        unlabelled_loss = F.l1_loss(unlabel_strong_op, unlabel_weak_op, reduction='none')
        output[slice_index:] = unlabel_weak_op
        unlabel_mask = torch.where(unlabelled_loss < self.confidence, 0, 1)

        #print('unlable ce loss\n', unlabelled_loss)
        unlabel_ce_loss = unlabel_weak_op * unlabel_mask

        #print('unlable ce loss after confidence\n', unlabel_ce_loss)
        unlabel_ce_loss = torch.mean(unlabel_ce_loss)

        #print('unlable ce loss after mean\n', unlabel_ce_loss)

        final_loss = label_ce_loss + self.weights * unlabel_ce_loss
        #print('final loss\n', final_loss)

        final_loss = label_ce_loss + self.weights * unlabel_ce_loss

        mean = mean.unsqueeze(1).unsqueeze(2)
        std = std.unsqueeze(1).unsqueeze(2)

        return {
            "batch_idx": batch_idx,
            "fname": fname,
            "slice_num": slice_num,
            "max_value": max_value,
            "output": output,
            "target": target * std + mean,
            "val_loss": final_loss
        }

    def test_step(self, batch, batch_idx):
        image, _, _, mean, std, fname, slice_num, _ = batch
        output, _ = self.forward(image)
        mean = mean.unsqueeze(1).unsqueeze(2)
        std = std.unsqueeze(1).unsqueeze(2)

        return {
            "fname": fname,
            "slice": slice_num,
            "output": (output * std + mean).cpu().numpy(),
        }

    def configure_optimizers(self):
        optim = torch.optim.RMSprop(
            self.parameters(),
            lr=self.lr,
            weight_decay=self.weight_decay,
        )
        scheduler = torch.optim.lr_scheduler.StepLR(
            optim, self.lr_step_size, self.lr_gamma
        )

        return [optim], [scheduler]

    @staticmethod
    def add_model_specific_args(parent_parser):  # pragma: no-cover
        """
        Define parameters that only apply to this model
        """
        parser = ArgumentParser(parents=[parent_parser], add_help=False)
        parser = MriModule.add_model_specific_args(parser)

        # network params
        parser.add_argument(
            "--in_chans", default=1, type=int, help="Number of U-Net input channels"
        )
        parser.add_argument(
            "--out_chans", default=1, type=int, help="Number of U-Net output chanenls"
        )
        parser.add_argument(
            "--chans", default=1, type=int, help="Number of top-level U-Net filters."
        )
        parser.add_argument(
            "--num_pool_layers",
            default=4,
            type=int,
            help="Number of U-Net pooling layers.",
        )
        parser.add_argument(
            "--drop_prob", default=0.0, type=float, help="U-Net dropout probability"
        )

        # training params (opt)
        parser.add_argument(
            "--lr", default=0.001, type=float, help="RMSProp learning rate"
        )
        parser.add_argument(
            "--lr_step_size",
            default=40,
            type=int,
            help="Epoch at which to decrease step size",
        )
        parser.add_argument(
            "--lr_gamma", default=0.1, type=float, help="Amount to decrease step size"
        )
        parser.add_argument(
            "--weight_decay",
            default=0.0,
            type=float,
            help="Strength of weight decay regularization",
        )

        return parser


